	/*
	 * Vocabulário:
	 * 
	 * Identifique a palavra do vocabulário para cada definição a seguir.
	 * 
	 * É um método disponível para uso sem primeiro criar uma instância da classe. Ela é
	 * declarada precedendo a definição com o modificador estático.
	 * Resposta: método static
	 * 
	 * Qualquer classe implementada como uma classe aninhada dentro de outra classe. Por
     * definição, todas as classes internas são membros da classe container por composição.
     * Resposta: classe interna ou inner class
     * 
     * Qualquer variável do nível de classe declarada com o modificador estático. Isso significa
     * que somente uma instância da variável de classe pode existir na JVM, independentemente
     * do número de instâncias de classe.
     * Resposta: variável static ou variável de classe
     * 
     * É uma palavra-chave que disponibiliza uma variável, um método ou uma classe interna sem
     * criar primeiro uma instância de uma variável.
     * Resposta: static
     * 
     * É uma classe interna. As classes internas são definidas dentro de uma classe pai ou de
     * container e são membros da classe de container por composição. Na verdade, as classes
     * internas são a única forma de criar instâncias de classe por meio de composição.
     * Resposta: classes aninhadas
     * 
     * É uma classe interna disponível para uso sem primeiro criar uma instância da classe de
     * container. Ela é declarada precedendo a definição com o modificador estático.
     * Resposta: classe static
     * 
     * Qualquer método Java definido com um modificador estático. É acessível fora da classe
     * quando um especificador de acesso público, protegido ou padrão o precede. É privado e
     * inacessível fora da classe quando um especificador privado o precede. Os métodos de      
     * classe são disponíveis sem primeiro criar uma instância da classe.
     * Resposta: método static
     * 
     * É uma variável que pode estar disponível fora de uma classe sem primeiro criar uma
     * instância de uma classe. Ela é declarada precedendo o nome da variável com o modificador
     * estático.
     * Resposta: variável static
     * 
     * Os exercícios desse submódulo estão no pacote veículos.
	 */
	
